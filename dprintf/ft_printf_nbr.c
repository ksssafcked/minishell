#include "ft_printf.h"

int	ft_printf_nbr(int fd, int n)
{
	char	c;
	int		i;

	i = 0;
	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		n = -n;
		write (fd, "-", 1);
		i++;
	}
	if (n >= 10)
		i += ft_printf_nbr(fd, n / 10);
	c = (n % 10) + '0';
	write (fd, &c, 1);
	i++;
	return (i);
}
