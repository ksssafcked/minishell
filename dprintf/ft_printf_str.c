#include "ft_printf.h"

int	ft_printf_str(int fd, char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		write (fd, "(null)", 6);
		return (i + 6);
	}
	while (s[i])
		write (fd, &s[i++], 1);
	return (i);
}
